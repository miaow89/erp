package org.kor.mv.modules.projectManage.Entity;

public class ProjectNameEntity {
	private String year;
	private String customerClassific;
	private String season;
	private String customerCompany;
	private String gender;
	private String goodsType;
	private String projectID;
	private String winStatus;
	private String projectNameCode;
	
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	
	public String getCustomerClassific() {
		return customerClassific;
	}
	public void setCustomerClassific(String customerClassific) {
		this.customerClassific = customerClassific;
	}
	public String getSeason() {
		return season;
	}
	public void setSeason(String season) {
		this.season = season;
	}
	public String getCustomerCompany() {
		return customerCompany;
	}
	public void setCustomerCompany(String customerCompany) {
		this.customerCompany = customerCompany;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getGoodsType() {
		return goodsType;
	}
	public void setGoodsType(String goodsType) {
		this.goodsType = goodsType;
	}
	public String getProjectID() {
		return projectID;
	}
	public void setProjectID(String projectID) {
		this.projectID = projectID;
	}
	public String getWinStatus() {
		return winStatus;
	}
	public void setWinStatus(String winStatus) {
		this.winStatus = winStatus;
	}
	public String getProjectNameCode() {
		return projectNameCode;
	}
	public void setProjectNameCode(String projectNameCode) {
		this.projectNameCode = projectNameCode;
	}
}
